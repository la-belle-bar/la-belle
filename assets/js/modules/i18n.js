  function format(template, params){
    return String(template).replace(/\{(\w+)\}/g, (_, key) => params?.[key] ?? '');
  }

  function getLanguage(){
    return localStorage.getItem(LS_LANG_KEY) || 'ru';
  }

  function setLanguage(lang){
    const next = dictionaries[lang] ? lang : 'ru';
    localStorage.setItem(LS_LANG_KEY, next);
    document.documentElement.lang = next === 'kk' ? 'kk' : 'ru';
    applyTranslations();
    document.dispatchEvent(new CustomEvent('lb:language-changed', {detail:{language:next}}));
  }

  function t(key, params){
    const lang = getLanguage();
    const value = dictionaries[lang]?.[key] || dictionaries.ru[key] || key;
    return params ? format(value, params) : value;
  }

  function applyTranslations(root = document){
    root.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = t(el.dataset.i18n);
    });
    root.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.setAttribute('placeholder', t(el.dataset.i18nPlaceholder));
    });
    root.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
      el.setAttribute('aria-label', t(el.dataset.i18nAriaLabel));
    });
    root.querySelectorAll('#languageSelect').forEach(el => {
      el.value = getLanguage();
    });
  }

  function init(){
    setLanguage(getLanguage());
    document.querySelectorAll('#languageSelect').forEach(select => {
      select.addEventListener('change', () => setLanguage(select.value));
    });
  }

  app.i18n = {init, t, setLanguage, getLanguage, applyTranslations};
